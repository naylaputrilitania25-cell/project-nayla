<?php
// Mendefinisikan variabel
$x = 10;
$y = 5;

// Penjumlahan
$hasil1 = $x + $y;
echo "Hasil penjumlahan " . $x . " dan " . $y . " adalah " . $hasil1 . "<br>";

// Pengurangan
$hasil2 = $x - $y;
echo "Hasil pengurangan " . $x . " dan " . $y . " adalah " . $hasil2 . "<br>";

// Perkalian
$hasil3 = $x * $y;
echo "Hasil perkalian " . $x . " dan " . $y . " adalah " . $hasil3 . "<br>";

// Pembagian
$hasil4 = $x / $y;
echo "Hasil pembagian " . $x . " dan " . $y . " adalah " . $hasil4 . "<br>";

// Modulo atau sisa bagi
$hasil5 = $x % $y;
echo "Hasil sisa bagi " . $x . " dan " . $y . " adalah " . $hasil5 . "<br>";
?>
